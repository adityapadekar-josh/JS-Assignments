/**
 * Share a minimum four differentiation between regular function and arrow functions with one examples
 */

// Syntax:
// Regular Function: Uses the function keyword.
// Arrow Function: Uses the => syntax.

// this Binding:
// Regular Function: Has its own this context.
// Arrow Function: Inherits this from the surrounding lexical context.

// Constructor Behavior:
// Regular Function: Can be used as a constructor with the new keyword.
// Arrow Function: Cannot be used as a constructor.

// Arguments Object:
// Regular Function: Has access to the arguments object, which contains all passed arguments.
// Arrow Function: Does not have its own arguments object, but inherits it from the surrounding scope.
